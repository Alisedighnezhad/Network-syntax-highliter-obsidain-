var import_obsidian = require("obsidian");
var DEFAULT_SETTINGS = {
  disabledLanguages: [],
  showLanguageBadge: true
};
var NetworkSyntaxSettingsTab = class extends import_obsidian.PluginSettingTab {
  constructor(app, plugin) {
    super(app, plugin);
    this.plugin = plugin;
  }
  display() {
    const { containerEl } = this;
    containerEl.empty();
    containerEl.createEl("h2", { text: "Network Syntax Highlighter" });
    containerEl.createEl("p", {
      text: "Enable or disable syntax highlighting per language. Changes take effect after reloading the plugin or restarting Obsidian.",
      cls: "setting-item-description"
    });
    containerEl.createEl("h3", { text: "Languages" });
    for (const lang of LANGUAGE_REGISTRY) {
      new import_obsidian.Setting(containerEl).setName(lang.name).setDesc(
        `Code block tags: \`${[lang.id, ...lang.aliases].join("`, `")}\``
      ).addToggle((toggle) => {
        toggle.setValue(!this.plugin.settings.disabledLanguages.includes(lang.id)).onChange(async (value) => {
          const disabled = this.plugin.settings.disabledLanguages;
          if (value) {
            const idx = disabled.indexOf(lang.id);
            if (idx !== -1)
              disabled.splice(idx, 1);
          } else {
            if (!disabled.includes(lang.id))
              disabled.push(lang.id);
          }
          await this.plugin.saveSettings();
        });
      });
    }
    containerEl.createEl("h3", { text: "Display" });
    new import_obsidian.Setting(containerEl).setName("Show language badge").setDesc("Display a small label in the top-right corner of highlighted blocks.").addToggle((toggle) => {
      toggle.setValue(this.plugin.settings.showLanguageBadge).onChange(async (value) => {
        this.plugin.settings.showLanguageBadge = value;
        await this.plugin.saveSettings();
      });
    });
    containerEl.createEl("h3", { text: "About" });
    containerEl.createEl("p", {
      text: "Supported languages: " + LANGUAGE_REGISTRY.map((l) => l.name).join(", ") + "."
    });
    containerEl.createEl("p", {
      text: "To add a new language, add a file to languages/ and register it in languages/index.ts.",
      cls: "setting-item-description"
    });
  }
};

// src/main.ts
var NetworkSyntaxPlugin = class extends import_obsidian2.Plugin {
  async onload() {
    await this.loadSettings();
    this.addSettingTab(new NetworkSyntaxSettingsTab(this.app, this));
    for (const lang of LANGUAGE_REGISTRY) {
      const ids = [lang.id, ...lang.aliases];
      for (const id of ids) {
        this.registerMarkdownCodeBlockProcessor(id, (source, el, _ctx) => {
          this.renderBlock(source, el, lang, id);
        });
      }
    }
    console.log("[NetworkSyntax] Loaded \u2014", LANGUAGE_REGISTRY.map((l) => l.id).join(", "));
  }
  renderBlock(source, el, lang, id) {
    el.empty();
    const wrap = el.createDiv({ cls: "network-syntax-block" });
    const pre = wrap.createEl("pre", { cls: `language-${id}` });
    const code = pre.createEl("code", { cls: `language-${id}` });
    code.innerHTML = lang.highlight(source);
    if (this.settings.showLanguageBadge) {
      wrap.createDiv({ cls: "network-syntax-badge", text: lang.name });
    }
  }
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
  }
};
