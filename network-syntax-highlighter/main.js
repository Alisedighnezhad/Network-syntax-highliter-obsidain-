var RULES = [
  // comment
  { type: "comment", pattern: sticky(/#.*/) },
  // strings
  { type: "string", pattern: sticky(/"[^"\\]*(?:\\[\s\S][^"\\]*)*"|'[^'\\]*(?:\\[\s\S][^'\\]*)*'/) },
  // IPv6 before IPv4
  { type: "ipv6", pattern: sticky(/(?:[0-9a-fA-F]{1,4}:){2,7}[0-9a-fA-F:]*(?:\/\d+)?/) },
  // IPv4 + CIDR
  { type: "ipv4", pattern: sticky(/\b(?:(?:25[0-5]|2[0-4]\d|[01]?\d\d?)\.){3}(?:25[0-5]|2[0-4]\d|[01]?\d\d?)(?:\/(?:3[0-2]|[12]?\d))?\b/) },
  // MAC address
  { type: "mac-address", pattern: sticky(/[0-9A-Fa-f]{2}(?::[0-9A-Fa-f]{2}){5}|[0-9A-Fa-f]{2}(?:-[0-9A-Fa-f]{2}){5}/) },
  // Duration
  { type: "duration", pattern: sticky(/(?:\d+[wdhms])+/) },
  // Percentage
  { type: "percentage", pattern: sticky(/\d+(?:\.\d+)?%/) },
  // Hex
  { type: "hexadecimal", pattern: sticky(/0x[0-9A-Fa-f]+/) },
  // Variable
  { type: "variable", pattern: sticky(/\$(?:"[^"]*"|\w+)/) },
  // Paths — longest first, must be followed by whitespace/= or end
  { type: "path", pattern: sticky(new RegExp(alt(PATHS) + "(?=\\s|=|$)")) },
  // Script keywords with colon
  { type: "script-keyword", pattern: sticky(new RegExp(alt(SCRIPT_COLON) + "(?=\\s|\\(|$)")) },
  // Script plain keywords — only as whole words
  { type: "script-keyword", pattern: sticky(new RegExp("\\b(?:" + SCRIPT_PLAIN.join("|") + ")\\b")) },
  // property=value — match key=val together, emit property + = + value spans
  { type: "_prop_val", pattern: sticky(new RegExp(alt(PROPERTIES) + "=[^\\s]*")) },
  // standalone commands
  { type: "command", pattern: sticky(new RegExp("\\b" + alt(COMMANDS) + "\\b")) },
  // firewall chain names
  { type: "firewall", pattern: sticky(/\b(?:input|output|forward|srcnat|dstnat|prerouting|postrouting|broute)\b/) },
  // protocols
  { type: "protocol", pattern: sticky(new RegExp("\\b" + alt(PROTOCOLS) + "\\b")) },
  // number
  { type: "number", pattern: sticky(/\b\d+(?:\.\d+)?\b/) },
  // operator
  { type: "operator", pattern: sticky(/!=|>=|<=|&&|\|\||->|[=<>!~+\-*/&|^]/) },
  // punctuation
  { type: "punctuation", pattern: sticky(/[{}[\]();,]/) }
];
function highlightPropVal(raw) {
  const eq = raw.indexOf("=");
  if (eq === -1)
    return `<span class="token property">${esc(raw)}</span>`;
  const key = raw.slice(0, eq);
  const val = raw.slice(eq + 1);
  const propSpan = `<span class="token property">${esc(key)}</span>`;
  const opSpan = `<span class="token operator">=</span>`;
  const knownVal = VALUES.find((v) => v === val);
  const valSpan = knownVal ? `<span class="token value">${esc(val)}</span>` : `<span class="token value">${esc(val)}</span>`;
  return propSpan + opSpan + valSpan;
}
function highlightRouterOS(source) {
  let out = "";
  let pos = 0;
  const len = source.length;
  while (pos < len) {
    let matched = false;
    for (const rule of RULES) {
      rule.pattern.lastIndex = pos;
      const m = rule.pattern.exec(source);
      if (m && m.index === pos) {
        if (rule.type === "_prop_val") {
          out += highlightPropVal(m[0]);
        } else {
          out += `<span class="token ${rule.type}">${esc(m[0])}</span>`;
        }
        pos += m[0].length;
        matched = true;
        break;
      }
    }
    if (!matched) {
      out += esc(source[pos]);
      pos++;
    }
  }
  return out;
}

